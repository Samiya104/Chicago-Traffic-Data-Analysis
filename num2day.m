function day_week = num2day(i)

% This function converts the numerical value assigned to the day of the
% week to its corresponding day

    if i == 1
        day_week = 'sunday';
       
    elseif i==2
        day_week = 'monday';
        
    elseif i==3
        day_week = 'tuesday';
        
    elseif i==4
        day_week = 'wednesday';
        
    elseif i==5
        day_week = 'thursday';
        
    elseif i==6
        day_week = 'friday';
        
    elseif i==7
        day_week = 'sunday';
        
   end
    
end

